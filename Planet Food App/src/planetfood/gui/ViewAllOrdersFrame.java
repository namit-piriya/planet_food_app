    private void loadAllOrders(){
        
        DefaultTableModel model = (DefaultTableModel) jtbAllOrders.getModel();
        model.getDataVector().removeAllElements();
        try{
        ArrayList<Order> orderList = OrderDao.getAllOrders();
        Object[] rows = new Object[8];
        for(Order o : orderList)
            {
                rows[0] = o.getOrdId();
                rows[1] = o.getOrdDate();
                rows[2] = o.getOrdAmount();
                rows[3] = o.getDiscount();
                rows[4] = o.getGst();
                rows[5] = o.getGstAmount();
                rows[6] = o.getGrandTotal();
                rows[7] = o.getUserId();
                
                model.addRow(rows);
            }
        }
        catch (SQLException ex) {
             JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
         }
    
    
    }

