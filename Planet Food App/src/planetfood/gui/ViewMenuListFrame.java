    public void loadCategories(){
        try
        {
          categories = CategoryDao.getAllCategoryId();
          Set catName = categories.keySet();
          Iterator<String> it = catName.iterator();
          while(it.hasNext())
               {
               CbProdCat.addItem(it.next());
               }
        }
        catch(SQLException ex){
         JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
        
    }
    
    public void showDataInTable(){
        try{
            productList1 = ProductDao.getAllData();
            if(productList1.isEmpty()==true)
                JOptionPane.showMessageDialog(null, "Sorry! No Products in Database","Error",JOptionPane.INFORMATION_MESSAGE);
            
            else{
                Object[] rows = new Object[5];
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                for(Product p : productList1){
                    rows[0] =  p.getProdId();
                    rows[1] =  p.getCatId();
                    rows[2] =  p.getProdName();
                    rows[3] =  p.getPrice();
                    rows[4] =  p.getIsActive();
                    model.addRow(rows);
                    }
                }
        
        }catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    }

