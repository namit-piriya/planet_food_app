    public void loadProductDetails(){
        try
        {
          categories = CategoryDao.getAllCategoryId();
          Set catName = categories.keySet();
          Iterator<String> it = catName.iterator();
          while(it.hasNext())
               {
               CbProdCat.addItem(it.next());
               }
        }
        catch(SQLException ex){
         JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
        
    }
        public void clearAll(){
        CbProdId.removeAllItems();
        txtProdName.setText("");
        txtProdPrice.setText("");
        } 
        
    public int validateInput(){
        int result =1;
        prodName = txtProdName.getText();
        if(CbIsActive.isSelected())
            prodIsActive = "Y";
        else
            prodIsActive = "N";
        
        try{
            prodPrice = Double.parseDouble(txtProdPrice.getText());
        }
        catch(Exception e){
            result =0;
        }
        if(prodName.isEmpty())
            result = -1;
       
        return result;
    
    }
