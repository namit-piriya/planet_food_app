    private void BtnSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveChangesActionPerformed
    if(txtCatName.getText()==null)
         JOptionPane.showMessageDialog(null,"Category Name can't be Empty","Empty",JOptionPane.ERROR_MESSAGE);
   
    try{
    
    String catName = txtCatName.getText();
    String catId = CBCatId.getSelectedItem().toString();
    Category c =new Category();
    c.setCatId(catId);
    c.setCatName(catName);
    boolean status = CategoryDao.updateCategory(c);
    if(status==true)
        JOptionPane.showMessageDialog(null,"Category Details Updated Successfully","Success!!",JOptionPane.INFORMATION_MESSAGE);
    else
        JOptionPane.showMessageDialog(null,"Category Details Can't be Updated ","Failed!!",JOptionPane.INFORMATION_MESSAGE);
        
    }catch(SQLException ex){
         JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    
    }//GEN-LAST:event_BtnSaveChangesActionPerformed

