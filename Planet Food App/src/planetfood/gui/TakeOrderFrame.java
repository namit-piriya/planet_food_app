    public void loadProductDetails(){
        try {
            
        CbProdCat.removeAllItems();
        categories = CategoryDao.getAllCategoryId();
        for(String catName : categories.keySet())
            CbProdCat.addItem(catName);
        ordId = OrderDao.getNewId();
        txtOrderId.setText(ordId);
        do{
            String discountStr = JOptionPane.showInputDialog("How much discount is offered ?");
            try{
                discountPer = Double.parseDouble(discountStr);
                lblDiscount.setText("Discount("+discountPer+"%)");
                break;
            }catch(Exception ex){
             JOptionPane.showMessageDialog(null,"Please set Numeric discount","Error",JOptionPane.ERROR_MESSAGE);
                    ex.printStackTrace();
            }
        }while(true);
        
        }catch(SQLException ex){
         JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    }
    
