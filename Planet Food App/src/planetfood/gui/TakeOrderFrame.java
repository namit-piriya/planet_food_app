     if(txtQuantity.getText().isEmpty())
          JOptionPane.showMessageDialog(null,"Please Enter Quantity","Error",JOptionPane.ERROR_MESSAGE);
     Object[] rows = new Object[5];
     DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
     String productId = CbProdId.getSelectedItem().toString();
     String prodName = txtProdName.getText();
     String price = txtPrice.getText();
     String quantity = txtQuantity.getText();
     double amt = Double.parseDouble(price) * Double.parseDouble(quantity); 
     rows[0] =  productId;
     rows[1] =  prodName;
     rows[2] =  price;
     rows[3] =  quantity;
     rows[4] =  amt;
     model.addRow(rows);
     
      actualTotal=actualTotal+amt;
      subTotal=actualTotal;
        
      txtOldSubTotal.setText(String.valueOf(subTotal));
      discountAmount=Math.round(discountPer*subTotal/100.0);
      
      txtDiscount.setText(String.valueOf(discountAmount));
      
      subTotal=Math.round(subTotal-discountAmount);
      txtSubTotal.setText(String.valueOf(subTotal));
      gstAmount=Math.round(5*subTotal/100.0);
      txtGstAmt.setText(String.valueOf(gstAmount));
      grandTotal=Math.round(subTotal+gstAmount);
      txtGrandTotal.setText(String.valueOf(grandTotal));
            
      OrderDetail obj=new OrderDetail();
            obj.setOrdId(ordId);
