    private void CbProdCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CbProdCatActionPerformed
    catName =CbProdCat.getSelectedItem().toString();
    catId = categories.get(catName);
    clearAll();
         try {
             productList = ProductDao.getProductsByCategory(catId);
             
             for(String prodId:productList.keySet())
                 CbProdId.addItem(prodId);
             }
         
         catch (SQLException ex) {
             JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
         }        // TODO add your handling code here:
    }//GEN-LAST:event_CbProdCatActionPerformed

    private void CbProdIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CbProdIdActionPerformed
    if( CbProdId.getSelectedItem() == null )
        return;
    prodId = CbProdId.getSelectedItem().toString();
    p = productList.get(prodId);
    txtProdName.setText(p.getProdName());
    txtProdPrice.setText(String.valueOf(p.getPrice()));  
    prodIsActive =  p.getIsActive();
    
    if(prodIsActive.equalsIgnoreCase("Y"))
        CbIsActive.setSelected(true);
    else
        CbIsActive.setSelected(false);// TODO add your handling code here:
    }//GEN-LAST:event_CbProdIdActionPerformed

