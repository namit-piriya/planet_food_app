    private void CbProdCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CbProdCatActionPerformed
    String catName =CbProdCat.getSelectedItem().toString();
    String catId = categories.get(catName);
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    while(model.getRowCount()>0)
        model.removeRow(0);
    if(CbProdCat.getSelectedItem().toString().equalsIgnoreCase("select category"))
    {
        try{
            productList1 = ProductDao.getAllData();
            if(productList1.isEmpty()==true)
                JOptionPane.showMessageDialog(null, "Sorry! No Products in Database","Error",JOptionPane.INFORMATION_MESSAGE);
            
            else{
                Object[] rows = new Object[5];
                for(Product p : productList1){
                    rows[0] =  p.getProdId();
                    rows[1] =  p.getCatId();
                    rows[2] =  p.getProdName();
                    rows[3] =  p.getPrice();
                    rows[4] =  p.getIsActive();
                    model.addRow(rows);
                    }
                }
        
        }catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    }    
    
    else{
    try{
            productList2 = ProductDao.getProductsByCategory(catId);
            if(productList2.isEmpty()==true)
                JOptionPane.showMessageDialog(null, "Sorry! No Products in Database","Error",JOptionPane.INFORMATION_MESSAGE);
            
            else{
                Object[] rows = new Object[5];
                 
                for(Product p : productList2.values()){
                    rows[0] =  p.getProdId();
                    rows[1] =  p.getCatId();
                    rows[2] =  p.getProdName();
                    rows[3] =  p.getPrice();
                    rows[4] =  p.getIsActive();
                    model.addRow(rows);
                    }
                }  
    
    }catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
        
    }//GEN-LAST:event_CbProdCatActionPerformed

