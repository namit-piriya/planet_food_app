    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
    int validate = validateInput();
    if(validate == 0)
        {
        JOptionPane.showMessageDialog(null,"Please input Product Name","Empty",JOptionPane.ERROR_MESSAGE);
                return;
        }
    if(validate == -1)
        {
        JOptionPane.showMessageDialog(null,"Please input Product Price","Empty",JOptionPane.ERROR_MESSAGE);
                return;
        }
    
    try{
    p.setCatId(catId);
    p.setPrice(prodPrice);
    p.setProdId(prodId);
    p.setIsActive(prodIsActive);
    p.setProdName(prodName);
    if(ProductDao.UpdateProduct(p)== true)
        JOptionPane.showMessageDialog(null,"Product Details Updated Successfully","Success!!",JOptionPane.INFORMATION_MESSAGE);
    else
        JOptionPane.showMessageDialog(null,"Product Details Can't be Updated ","Failed!!",JOptionPane.INFORMATION_MESSAGE);
    
    }catch(SQLException ex){
         JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
     }
    // TODO add your handling code here:
    }//GEN-LAST:event_BtnSaveActionPerformed

