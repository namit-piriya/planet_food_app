    private void BtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSearchActionPerformed
    if(validateInput()==false){
        JOptionPane.showMessageDialog(null,"Please fill the field","Empty",JOptionPane.ERROR_MESSAGE);
        return;
    }
    try{
        Emp e = EmpDao.getEmpById(EmpId);
        if(e.getEname()==null)
            {
                JOptionPane.showMessageDialog(null,"No Empolyee found with such EmpId  ! Try again ! ","Empty",JOptionPane.ERROR_MESSAGE);
                return;
            }
        CbJob.setSelectedItem(e.getJob());
        txtEmpName.setText(e.getEname());
        txtSal.setText(String.valueOf(e.getSal()));
    }
    catch(SQLException ex){
         JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_BtnSearchActionPerformed

