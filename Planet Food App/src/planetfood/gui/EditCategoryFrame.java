    private void CBCatIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CBCatIdActionPerformed
    if(CBCatId.getSelectedItem()==null)
        return;
    String catId =CBCatId.getSelectedItem().toString();
    Set s = (Set) categoryList.entrySet();
        Iterator it = s.iterator();
        while(it.hasNext()){   
                  Map.Entry entry = (Map.Entry) it.next();
                  String catName = (String) entry.getKey();
                  String catid = (String)entry.getValue();
                  if(catid.equals(catId)){
                      txtCatName.setText(catName);
                      break;
                  }
        }
    }//GEN-LAST:event_CBCatIdActionPerformed

