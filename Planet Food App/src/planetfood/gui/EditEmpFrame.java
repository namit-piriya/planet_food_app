    private void BtnAddEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddEmpActionPerformed
    try{
    String Job = CbJob.getSelectedItem().toString();
    String Ename = txtEmpName.getText();
    Double sal = Double.parseDouble(txtSal.getText());
    
    Emp e = new Emp();
    e.setEmpid(EmpId);
    e.setEname(Ename);
    e.setJob(Job);
    e.setSal(sal);
    boolean status = EmpDao.updateEmployee(e);
    if(status==true)
        JOptionPane.showMessageDialog(null,"Employee Details Updated Successfully","Success!!",JOptionPane.INFORMATION_MESSAGE);
    else
        JOptionPane.showMessageDialog(null,"Employee Details Can't be Updated ","Failed!!",JOptionPane.INFORMATION_MESSAGE);
        
    }
    catch(SQLException ex){
         JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    catch(NumberFormatException ex){
        JOptionPane.showMessageDialog(null,"Please Enter Numeric value for Salary"+ex,"Input Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
    }
    }//GEN-LAST:event_BtnAddEmpActionPerformed

